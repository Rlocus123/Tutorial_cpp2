#include <iostream>

/* -----------------------------------------------------------------------------
Local includes
----------------------------------------------------------------------------- */
#include "Circle.hpp"

/* -----------------------------------------------------------------------------
Local defines
----------------------------------------------------------------------------- */
using namespace std;

/* -----------------------------------------------------------------------------
Constructor (s)
----------------------------------------------------------------------------- */
Circle::Circle(void)
{
    radius = 0 ;
}

Circle::Circle(double rad)
{
    if(rad >= 0.0) {
        radius = rad;
    }
}

/* -----------------------------------------------------------------------------
Initialize private variables
----------------------------------------------------------------------------- */
void Circle::init(double rad)
{
    if(rad >= 0.0) {
        radius = rad;
    }
}

/* -----------------------------------------------------------------------------
Compute area
----------------------------------------------------------------------------- */
double Circle::get_area(void)
{
    return PI * radius * radius ;
}

/* -----------------------------------------------------------------------------
Get diameter
----------------------------------------------------------------------------- */
double Circle::get_diameter(void)
{
    return 2.0 * radius;
}
